import { defineConfig, loadEnv } from 'vite'
import react from '@vitejs/plugin-react'

// https://vitejs.dev/config/
export default defineConfig(({ mode }) => {
  // Load env file based on `mode` in the current working directory.
  // Set the third parameter to '' to load all env regardless of the `VITE_` prefix.
  // @ts-ignore
  const env = loadEnv(mode, process.cwd(), '')

  return {
    base: './', // Use relative base path to ensure assets load correctly in any deployment/preview subpath
    plugins: [react()],
    define: {
      // Safely define process.env.API_KEY to be replaced by the string value during build
      'process.env.API_KEY': JSON.stringify(env.API_KEY || process.env.API_KEY || ''),
    },
    server: {
      cors: true // Enable CORS for local development
    }
  }
})